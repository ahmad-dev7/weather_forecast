String getImage(String imageCode) {
  // ignore: unused_local_variable
  String imagePath = '';
  switch (imageCode) {
    case '01d':
      return imagePath = 'images/01d.png';
    case '01n':
      return imagePath = 'images/01n.png';
    case '02d':
      return imagePath = 'images/02d.png';
    case '02n':
      return imagePath = 'images/02n.png';
    case '03d':
      return imagePath = 'images/03d.png';
    case '03n':
      return imagePath = 'images/03n.png';
    case '04d':
      return imagePath = 'images/02d.png';
    case '04n':
      return imagePath = 'images/02n.png';
    case '09d':
      return imagePath = 'images/09d.png';
    case '09n':
      return imagePath = 'images/09n.png';
    case '10d':
      return imagePath = 'images/09d.png';
    case '10n':
      return imagePath = 'images/09n.png';
    case '11d':
      return imagePath = 'images/11d.png';
    case '11n':
      return imagePath = 'images/11n.png';
    case '13d':
      return imagePath = 'images/13d.png';
    case '13n':
      return imagePath = 'images/13n.png';
    case '50d':
      return imagePath = 'images/50d.png';
    case '50n':
      return imagePath = 'images/50n.png';
    default:
      return imagePath = 'images/01n.png';
  }
}
